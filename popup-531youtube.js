window.onload = function() {
  var button = document.getElementById("get-verse");
  var popup = document.getElementById("popup");
  var verse = document.getElementById("verse");
  var youtubeLink = document.getElementById("youtube");

  button.addEventListener("click", function() {
    var randomVerse = verses[Math.floor(Math.random() * verses.length)];
    var lyric = randomVerse.lyric;
    var song = randomVerse.song;
    var link = randomVerse.youtubeLink;

    lyric = lyric.replace(/\n/g, "<br><br>"); // Add line breaks
    lyric = lyric.replace(/(Song:.+)/g, "<strong>$1</strong>"); // Bold the portion within the verse
    verse.innerHTML = lyric;
    youtubeLink.href = link;
    popup.style.display = "flex";
  });
};

